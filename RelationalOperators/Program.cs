//RelationalOperators

var x = 10;
var y = 20;

Console.WriteLine(x == y); // Is this equal to each other?
Console.WriteLine(x.Equals(y)); // Is this equal to each other? Using Equals Method refering Y
Console.WriteLine(x > y); // Is X greater than Y?
Console.WriteLine(x < y); // IS Y smaller than Y?
Console.WriteLine(x >= y); // Is X greater than or equal to X?
Console.WriteLine(x <= y); // Is X smaller than or equal to X?
Console.WriteLine(x != y); // Is X non equal or different of Y?
