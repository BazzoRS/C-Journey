// Atribuition Operators

var old = 10; //old will receive 10 INT


Console.WriteLine($"The initial value of X is {old}");
Console.WriteLine($"The value of X using the += operator is: {old += 5}");
Console.WriteLine($"The value of X using the -= operator is: {old -= 5}");
Console.WriteLine($"The value of X using the *= operator is: {old *= 5}");
Console.WriteLine($"The value of X using the /= operator is: {old /= 5}");
Console.WriteLine($"The value of X using the %= operator is: {old %= 5}");

